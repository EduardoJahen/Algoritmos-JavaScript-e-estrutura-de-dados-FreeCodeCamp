//Concatenando Strings com o Operador Plus Equals Também podemos usar o +=operador para concatenar uma string no final de uma variável de string existente. Isso pode ser muito útil para quebrar uma longa sequência em várias linhas.
//Nota: Cuidado com os espaços. A concatenação não adiciona espaços entre strings concatenadas, então você mesmo precisará adicioná-las.

let myStr = "This is the first sentence. "
myStr += "This is the second sentence."
console.log(myStr)